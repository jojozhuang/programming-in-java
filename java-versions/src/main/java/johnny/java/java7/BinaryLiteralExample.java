package johnny.java.java7;

public class BinaryLiteralExample {
    public static void main(String args[]) {
        int i = 0b0111;
        byte b = (byte) 0b0111;
        long l= (long) 0B0111;
        System.out.println("i = " + i);
        System.out.println("b = " + b);
        System.out.println("l = " + l);
    }

}
